export function detectLanguage() {
  const saved = localStorage.getItem("lifexp_language");
  if (saved === "es" || saved === "en") return saved;
  const browserLanguage = navigator.language?.toLowerCase() || "";
  if (browserLanguage.startsWith("en")) return "en";
  return "es";
}

export function t(state, path, params = {}) {
  const language = state?.language || detectLanguage();
  const value = path.split(".").reduce((current, key) => current?.[key], translations[language]) ??
    path.split(".").reduce((current, key) => current?.[key], translations.es) ??
    path;

  return Object.entries(params).reduce(
    (text, [key, replacement]) => String(text).replaceAll(`{${key}}`, replacement),
    value
  );
}
