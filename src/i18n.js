export function localizeVisibleText(root, state) {
  const language = state?.language || "es";
  const dictionary = visibleTextTranslations[language] || {};
  if (language !== "en") return;

  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);

  nodes.forEach((node) => {
    const raw = node.nodeValue;
    const trimmed = raw.trim();
    if (!trimmed) return;
    if (dictionary[trimmed]) {
      node.nodeValue = raw.replace(trimmed, dictionary[trimmed]);
      return;
    }
    for (const [source, target] of Object.entries(dictionary)) {
      if (trimmed.includes(source)) {
        node.nodeValue = raw.replace(source, target);
      }
    }
  });

  root.querySelectorAll("input[placeholder], textarea[placeholder]").forEach((element) => {
    const value = element.getAttribute("placeholder");
    if (dictionary[value]) element.setAttribute("placeholder", dictionary[value]);
  });
}
